import random
from pprint import pprint
import numpy as np

def count_units(x,y):
	a = np.zeros((max(x) + 2, max(y) + 2))
	for xx, yy in zip(x,y):
		a[xx][yy] = 1.0

	from collections import deque

	s = deque()
	t = []

	i = random.randint(0, len(y) - 1)

	m = x[i]
	n = y[i]

	temp = (m,n)

	s.append(temp)
	count = 0
	while(len(s) != 0):
		count += 1
		m, n = s.popleft()
		if a[m+1][n] == 1.0 and (m+1, n) not in t and (m+1, n) not in s:
			s.append((m+1, n))
		if a[m-1][n] == 1.0 and (m-1, n) not in t and (m-1, n) not in s:
			s.append((m-1, n))
		if a[m][n+1] == 1.0 and (m, n+1) not in t and (m, n+1) not in s:
			s.append((m, n+1))
		if a[m][n-1] == 1.0 and (m, n-1) not in t and (m, n-1) not in s:
			s.append((m, n-1))
		t.append((m,n))

	return count

